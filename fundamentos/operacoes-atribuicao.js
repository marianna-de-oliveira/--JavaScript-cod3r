const a = 7 //atribuindo o valor 7 na constante
let b = 4

b += a // atribuição aditiva, mesma coisa que dizer b = b + a
console.log(b)
b -= 4 // atribuição subtrativa,  b = b - 4
console.log(b)
b *= 2 // atribuição multiplicativa, b = b * 2
console.log(b)
b /= 2 // atribuição divisora, b = b / 2
console.log(b)
b %= 2 // atribuição modular, b = b % 2, se o valor de b for par, o resultado é 0, se b for impar o valor é 1
console.log(b)